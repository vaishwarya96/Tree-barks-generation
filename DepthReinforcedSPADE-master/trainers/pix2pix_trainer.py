from models.networks.sync_batchnorm import DataParallelWithCallback
from models.pix2pix_model import Pix2PixModel
from models.dual_model import DualModel

class DualTrainer():
    """
    Trainer creates the model and optimizers, and uses them to
    updates the weights of the network while reporting losses
    and the latest visuals to visualize the progress in training.
    """

    def __init__(self, opt):
        self.opt = opt
        self.dual_model = DualModel(opt)
        if len(opt.gpu_ids) > 0:
            self.dual_model = DataParallelWithCallback(self.dual_model,
                                                          device_ids=opt.gpu_ids)
            self.dual_model_on_one_gpu = self.dual_model.module
        else:
            self.dual_model_on_one_gpu = self.dual_model

        self.generated = None
        if opt.isTrain:
            self.optimizer_G, self.optimizer_D1, self.optimizer_D2 = \
                self.dual_model_on_one_gpu.create_optimizers(opt)
            self.old_lr = opt.lr

    def run_generator_one_step(self, data):
        self.optimizer_G.zero_grad()
        g_losses, generated_surface, generated_color = self.dual_model(data, mode='generator')
        g_loss = sum(g_losses.values()).mean()
        g_loss.backward()
        self.optimizer_G.step()
        self.g_losses = g_losses
        self.generated_surface = generated_surface
        self.generated_color = generated_color

    def run_discriminator_one_step(self, data):
        self.optimizer_D1.zero_grad()
        d1_losses = self.dual_model(data, mode='surface_discriminator')
        d1_loss = sum(d1_losses.values()).mean()
        d1_loss.backward()
        self.optimizer_D1.step()
        self.d1_losses = d1_losses

        self.optimizer_D2.zero_grad()
        d2_losses = self.dual_model(data, mode='color_discriminator')
        d2_loss = sum(d2_losses.values()).mean()
        d2_loss.backward()
        self.optimizer_D2.step()
        self.d2_losses = d2_losses


    def get_latest_losses(self):
        return {**self.g_losses, **self.d1_losses, **self.d2_losses}

    def get_latest_generated(self):
        return self.generated_surface, self.generated_color

    def update_learning_rate(self, epoch):
        self.update_learning_rate(epoch)

    def save(self, epoch):
        self.dual_model_on_one_gpu.save(epoch)

    ##################################################################
    # Helper functions
    ##################################################################

    def update_learning_rate(self, epoch):
        if epoch > self.opt.niter:
            lrd = self.opt.lr / self.opt.niter_decay
            new_lr = self.old_lr - lrd
        else:
            new_lr = self.old_lr

        if new_lr != self.old_lr:
            if self.opt.no_TTUR:
                new_lr_G = new_lr
                new_lr_D = new_lr
            else:
                new_lr_G = new_lr / 2
                new_lr_D = new_lr * 2

            for param_group in self.optimizer_D1.param_groups:
                param_group['lr'] = new_lr_D

            for param_group in self.optimizer_D2.param_groups:
                param_group['lr'] = new_lr_D

            for param_group in self.optimizer_G.param_groups:
                param_group['lr'] = new_lr_G
            print('update learning rate: %f -> %f' % (self.old_lr, new_lr))
            self.old_lr = new_lr

